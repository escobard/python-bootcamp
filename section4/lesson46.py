## negative index offset
fruits = ["Strawberries", "Nectarines", "Apples", "Grapes", "Peaches", "Cherries", "Pears"]
## this grabs the "Apples" string, since starting from the last item, apples is in index -5
### opposite to positive index offsets, negative offsets start at -1, not 0
print(fruits[-5])

## array.append adds an item after the last index in the array
fruids.append('Lemons')